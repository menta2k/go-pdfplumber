package extract

// Standard 14 font glyph widths (in 1/1000 of a unit).
// Source: PDF Reference, Appendix D.
// Only the most commonly used fonts are included; others fall back to 600 (monospace default).

var standardFontWidths = map[string]map[byte]float64{
	"Helvetica":            helveticaWidths,
	"Helvetica-Bold":       helveticaBoldWidths,
	"Helvetica-Oblique":    helveticaWidths, // same metrics as regular
	"Helvetica-BoldOblique": helveticaBoldWidths,
	"Times-Roman":          timesRomanWidths,
	"Times-Bold":           timesBoldWidths,
	"Times-Italic":         timesItalicWidths,
	"Times-BoldItalic":     timesBoldItalicWidths,
	"Courier":              courierWidths,
	"Courier-Bold":         courierWidths,
	"Courier-Oblique":      courierWidths,
	"Courier-BoldOblique":  courierWidths,
	"Symbol":               nil, // use default
	"ZapfDingbats":         nil, // use default
}

// fontWidth returns the width of a character code in a given font (in 1/1000 units).
func fontWidth(fontName string, code byte) float64 {
	if widths, ok := standardFontWidths[fontName]; ok && widths != nil {
		if w, found := widths[code]; found {
			return w
		}
	}
	return 600 // default monospace width
}

// isStandardFont reports whether the font name is one of the standard 14 fonts.
func isStandardFont(name string) bool {
	_, ok := standardFontWidths[name]
	return ok
}

var courierWidths = func() map[byte]float64 {
	m := make(map[byte]float64)
	for i := 0; i < 256; i++ {
		m[byte(i)] = 600
	}
	return m
}()

